    private void btnGenKeyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenKeyActionPerformed
        resetAllFeild();
        int max = 45000, min = 8000;
        int p, a, alpha, beta;
        Random genaration = new Random();
        do {
            p = genaration.nextInt((max - min) + 1) + min;
        } while (CheckSNT(p) == false || p <= 2);

        jtfP.setText("" + p);
        jtfPCrypto.setText("" + p);
        //set a
        a = genaration.nextInt((p - 2) + 1) + 2;
        jtfA.setText("" + a);
        //set alpha
        alpha = genaration.nextInt((p - 1) + 1) + 1;
        jtfAlpha.setText("" + alpha);
        //set beta
        beta = BinhPhuongNhan(alpha, a, p);
        jtfBeta.setText("" + beta);
        
        
        btnEncrypt.setEnabled(true);
    }//GEN-LAST:event_btnGenKeyActionPerformed

    private void btnEncryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncryptActionPerformed
        if (jtaEncryptPlainText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please Enter Plaint Text That You Want To Encrypt!",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            ArrayList<Integer> arr_ban_ma = new ArrayList<>();
            jtaEncryptCipherText.setText("");
            jtaDecryptCipherText.setText("");
            jtaDecryptPlainText.setText("");
            // TODO add your handling code here:
            Random genaration = new Random();
            int p = Integer.valueOf(jtfP.getText());
            int alpha = Integer.valueOf(jtfAlpha.getText());
            int k = genaration.nextInt((p - 2) + 1);
            int beta = Integer.valueOf(jtfBeta.getText());
            int y1 = BinhPhuongNhan(alpha, k, p);
            int y2;
            String ban_ro = jtaEncryptPlainText.getText();
            String ban_ma = "";

            int i = 0;
            while (i < ban_ro.length()) {
                arr_ban_ma.add(y1);
                y2 = ((ban_ro.charAt(i) * BinhPhuongNhan(beta, k, p)) % p);
                arr_ban_ma.add(y2);
                i++;
            }

            // chuoi ma hoa dang chu
            for (int j = 0; j < arr_ban_ma.size(); j++) {
                ban_ma += (char) (arr_ban_ma.get(j) % p);
            }
            jtaEncryptCipherText.setText(ban_ma);

            // e y1= alpha ^k  mod p
            // e y2 = x* beta^k mod p

            jtaDecryptCipherText.setText(ban_ma);
            btnDecrypt.setEnabled(true);
        }
    }//GEN-LAST:event_btnEncryptActionPerformed

    private void btnDecryptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDecryptActionPerformed
        ArrayList<Integer> arr_ban_ma = new ArrayList<>();
        String banMa = jtaDecryptCipherText.getText();
        int p = Integer.valueOf(jtfP.getText());
        int a = Integer.valueOf(jtfA.getText());
        int k = p - a - 1;
        
        int j=0;
        while (j < banMa.length()) {
            arr_ban_ma.add((int)banMa.charAt(j));
            arr_ban_ma.add((int)banMa.charAt(++j));
            j++;
        }
        
        int y1 = BinhPhuongNhan(arr_ban_ma.get(0), k, p);
        String ban_ro = "";

        for (int i = 1; i < arr_ban_ma.size(); i += 2) {
            ban_ro += (char) ((arr_ban_ma.get(i) * y1) % p);
        }
        jtaDecryptPlainText.setText(ban_ro);
    }//GEN-LAST:event_btnDecryptActionPerformed

