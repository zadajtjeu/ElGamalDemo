    //check so nguyen to
    private boolean CheckSNT(int n) {
        // so nguyen n < 2 khong phai la so nguyen to
        if (n < 2) {
            return false;
        }
        // check so nguyen to khi n >= 2
        for (int i = 2; i <= Math.sqrt(n); i++) {
            if (n % i == 0) {
                return false;
            }
        }
        return true;
    }
    
    private int BinhPhuongNhan(int alpha, int a, int p) {
        int ketqua = 1, n = p, x = alpha;
        String k = Integer.toBinaryString(a);//chuyen sang nhi phan
        int i = 0;
        while (i < k.length()) {
            ketqua *= ketqua;
            ketqua %= n;
            if (k.charAt(i) == '1') {
                ketqua *= x;
            }
            ketqua %= n;
            i++;
        }
        return ketqua;
    }
    
    
        
